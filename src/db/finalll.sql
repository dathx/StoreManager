create proc sp_sanPham(@thang int, @nam int)
as 
begin
	SELECT month(ngaymua),year(ngaymua),SANPHAM.TENSP,CHITIETDONHANG.MASP,SUM(CHITIETDONHANG.GIA) as N'giá' ,COUNT(CHITIETDONHANG.SOLUONG)
	FROM DONHANG,CHITIETDONHANG,SANPHAM
	WHERE DONHANG.MADH=CHITIETDONHANG.MADH and CHITIETDONHANG.MASP=SANPHAM.MASP and month(ngaymua) = @thang and year(ngaymua) = @nam
	GROUP BY NGAYMUA,DONHANG.MADH,CHITIETDONHANG.MASP,SANPHAM.TENSP
end

