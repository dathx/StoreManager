create proc dh_DonHangFull
as begin
select dh.MADH,
       cast(NGAYMUA as varchar)as NGAYMUA,
	   sp.TENSP,
	   tt.TENTT,
	   ct.SOLUONG,
	   cast(sp.GIA as numeric)as GIA,
	   cast((ct.SOLUONG*sp.GIA) as numeric) as THANHTIEN,
	   TENKH,
	   nv.TENNV
from donhang dh 
join CHITIETDONHANG ct on dh.MADH=ct.MADH 
join SANPHAM sp on ct.MASP= sp.MASP
join KHACHHANG kh on kh.MAKH = dh.MAKH
join THANHTOAN tt on dh.MATT = tt.MATT
join NHANVIEN nv on dh.MANV = nv.MANV
order by dh.MADH desc
end

