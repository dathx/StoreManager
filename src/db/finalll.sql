create proc sp_DoanhThuSanPham(@MASP int) 
as
begin
    select 
	dh.MASP,
	sp.TENSP,
	SUM(dh.GIA) Gia,
	Count(dh.soluong) SoLuong
	from SANPHAM sp join CHITIETDONHANG dh on sp.MASP=dh.MASP
	where @MASP = dh.masp
	group by dh.MASP,sp.TENSP
end


